public class Literals   // Literals:- A constant value which can be assigned to any variable.
{
    public static void main(String[] args) {
        byte age = 20;      // Integer literal
        char ch = 'A';      // charecter literal
        float f1 = 5.6f;    //double literal :- default
        double d1 = 5.66;   //Double literal
        System.out.println(age);
    }
}